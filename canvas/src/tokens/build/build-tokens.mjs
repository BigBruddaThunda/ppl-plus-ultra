/**
 * build-tokens.mjs
 *
 * style-dictionary v4 build script for the Ppl± design token system.
 *
 * Reads:  canvas/src/tokens/design-tokens.json
 * Writes: canvas/src/tokens/design-tokens.css  (CSS custom properties)
 *         canvas/src/tokens/tokens.ts           (TypeScript typed exports)
 *
 * CSS naming convention: double-hyphen nesting
 *   Token path ['colors', 'passion', 'primary'] -> --ppl-color-passion--primary
 *   Token path ['orders', 'strength', 'fontWeight'] -> --ppl-order-strength--fontWeight
 *   Token path ['axes', 'basics', 'gradientDirection'] -> --ppl-axis-basics--gradientDirection
 *
 * Usage:
 *   node src/tokens/build/build-tokens.mjs
 *   npm run build:tokens
 */

import StyleDictionary from 'style-dictionary';
import { readFileSync, writeFileSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const TOKENS_DIR = join(__dirname, '..');
const SOURCE_FILE = join(TOKENS_DIR, 'design-tokens.json');
const CSS_OUT = join(TOKENS_DIR, 'design-tokens.css');
const TS_OUT = join(TOKENS_DIR, 'tokens.ts');

// ─── Read source ──────────────────────────────────────────────────────────────

const source = JSON.parse(readFileSync(SOURCE_FILE, 'utf8'));

// ─── Flatten tokens for style-dictionary ─────────────────────────────────────
// style-dictionary expects { [category]: { [group]: { [prop]: { value: ... } } } }
// We need a 3-level structure: category -> name -> property -> { value }

function flattenForSD(sourceJson) {
  const flat = {};

  // Colors: colors.passion.primary -> { value: "oklch(...)" }
  for (const [tonalName, colorEntry] of Object.entries(sourceJson.colors)) {
    if (!flat.colors) flat.colors = {};
    flat.colors[tonalName] = {};
    const paletteProps = ['primary', 'secondary', 'background', 'surface', 'text', 'accent', 'border'];
    for (const prop of paletteProps) {
      if (colorEntry[prop] !== undefined) {
        flat.colors[tonalName][prop] = { value: colorEntry[prop] };
      }
    }
  }

  // Orders: orders.strength.fontWeight -> { value: 800 }
  for (const [orderSlug, orderEntry] of Object.entries(sourceJson.orders)) {
    if (!flat.orders) flat.orders = {};
    flat.orders[orderSlug] = {};
    const orderProps = ['fontWeight', 'fontWeightDisplay', 'lineHeight', 'spacingMultiplier', 'letterSpacing', 'density'];
    for (const prop of orderProps) {
      if (orderEntry[prop] !== undefined) {
        flat.orders[orderSlug][prop] = { value: orderEntry[prop] };
      }
    }
  }

  // Axes: axes.basics.gradientDirection -> { value: "to bottom" }
  for (const [axisSlug, axisEntry] of Object.entries(sourceJson.axes)) {
    if (!flat.axes) flat.axes = {};
    flat.axes[axisSlug] = {};
    const axisProps = ['gradientDirection', 'layoutFlow', 'typographyBias'];
    for (const prop of axisProps) {
      if (axisEntry[prop] !== undefined) {
        flat.axes[axisSlug][prop] = { value: axisEntry[prop] };
      }
    }
  }

  return flat;
}

const flatTokens = flattenForSD(source);

// ─── Custom CSS variable name transform ───────────────────────────────────────
// Path: ['colors', 'passion', 'primary'] -> 'ppl-color-passion--primary'
// Category plurals are stripped: colors->color, orders->order, axes->axis

function pplCssVarName(path) {
  const categoryMap = { colors: 'color', orders: 'order', axes: 'axis' };
  const [category, group, prop] = path;
  const cat = categoryMap[category] ?? category;
  return `ppl-${cat}-${group}--${prop}`;
}

// ─── Build CSS output directly ────────────────────────────────────────────────
// We build CSS directly rather than using style-dictionary's CSS platform
// to ensure exact double-hyphen naming control.

function buildCSS(sourceJson) {
  const lines = [
    '/* Ppl± Design Tokens — generated by build-tokens.mjs */',
    '/* DO NOT EDIT — source of truth is design-tokens.json */',
    '',
    ':root {',
  ];

  // Colors
  lines.push('  /* ─── Colors ──────────────────────────────────────────────── */');
  for (const [tonalName, colorEntry] of Object.entries(sourceJson.colors)) {
    const paletteProps = ['primary', 'secondary', 'background', 'surface', 'text', 'accent', 'border'];
    for (const prop of paletteProps) {
      if (colorEntry[prop] !== undefined) {
        lines.push(`  --ppl-color-${tonalName}--${prop}: ${colorEntry[prop]};`);
      }
    }
  }

  // Orders
  lines.push('');
  lines.push('  /* ─── Orders ──────────────────────────────────────────────── */');
  for (const [orderSlug, orderEntry] of Object.entries(sourceJson.orders)) {
    const orderProps = ['fontWeight', 'fontWeightDisplay', 'lineHeight', 'spacingMultiplier', 'letterSpacing', 'density'];
    for (const prop of orderProps) {
      if (orderEntry[prop] !== undefined) {
        lines.push(`  --ppl-order-${orderSlug}--${prop}: ${orderEntry[prop]};`);
      }
    }
  }

  // Axes
  lines.push('');
  lines.push('  /* ─── Axes ────────────────────────────────────────────────── */');
  for (const [axisSlug, axisEntry] of Object.entries(sourceJson.axes)) {
    const axisProps = ['gradientDirection', 'layoutFlow', 'typographyBias'];
    for (const prop of axisProps) {
      if (axisEntry[prop] !== undefined) {
        lines.push(`  --ppl-axis-${axisSlug}--${prop}: ${axisEntry[prop]};`);
      }
    }
  }

  lines.push('}');
  return lines.join('\n') + '\n';
}

// ─── Build TypeScript output ──────────────────────────────────────────────────

function buildTypeScript(sourceJson) {
  // Build the tokens object shape from the source JSON
  // Remove metadata fields from Color entries (only keep palette + key metadata)
  const colorsOut = {};
  for (const [tonalName, colorEntry] of Object.entries(sourceJson.colors)) {
    const { emoji, scl_name, tonal_name, base_hue, base_chroma, tier, gold, ...palette } = colorEntry;
    colorsOut[tonalName] = {
      meta: { emoji, scl_name, tonal_name, base_hue, base_chroma, tier, gold },
      ...palette,
    };
  }

  // Orders — keep all fields
  const ordersOut = {};
  for (const [slug, entry] of Object.entries(sourceJson.orders)) {
    ordersOut[slug] = { ...entry };
  }

  // Axes — keep all fields
  const axesOut = {};
  for (const [slug, entry] of Object.entries(sourceJson.axes)) {
    axesOut[slug] = { ...entry };
  }

  const tokensJson = JSON.stringify({ colors: colorsOut, orders: ordersOut, axes: axesOut }, null, 2);

  return `/**
 * tokens.ts — Ppl± Design Tokens
 *
 * Generated by build-tokens.mjs from design-tokens.json
 * DO NOT EDIT — source of truth is design-tokens.json
 *
 * Exports:
 *   tokens          — complete typed token object
 *   TonalColorName  — union of 8 tonal color names
 *   OrderName       — union of 7 Order slugs
 *   AxisName        — union of 6 Axis slugs
 *   COLOR_W_TO_TONAL — W enum position (19-26) -> tonal name bridge table
 */

// ─── Token object ──────────────────────────────────────────────────────────────

export const tokens = ${tokensJson} as const;

// ─── Type aliases ──────────────────────────────────────────────────────────────

export type TonalColorName = keyof typeof tokens.colors;
export type OrderName = keyof typeof tokens.orders;
export type AxisName = keyof typeof tokens.axes;

// ─── COLOR_W_TO_TONAL bridge table ────────────────────────────────────────────
// Maps W enum positions from canvas/src/types/scl.ts (W.TEACHING=19 .. W.MINDFUL=26)
// to tonal token key names used in tokens.colors
//
// W enum:
//   TEACHING=19, BODYWEIGHT=20, STRUCTURED=21, TECHNICAL=22,
//   INTENSE=23, CIRCUIT=24, FUN=25, MINDFUL=26

export const COLOR_W_TO_TONAL: Record<number, TonalColorName> = {
  19: 'order',         // W.TEACHING  ⚫
  20: 'growth',        // W.BODYWEIGHT 🟢
  21: 'planning',      // W.STRUCTURED 🔵
  22: 'magnificence',  // W.TECHNICAL  🟣
  23: 'passion',       // W.INTENSE    🔴
  24: 'connection',    // W.CIRCUIT    🟠
  25: 'play',          // W.FUN        🟡
  26: 'eudaimonia',    // W.MINDFUL    ⚪
} as const;
`;
}

// ─── Execute builds ───────────────────────────────────────────────────────────

console.log('Building design tokens...');

const cssOutput = buildCSS(source);
writeFileSync(CSS_OUT, cssOutput);
console.log(`  CSS -> ${CSS_OUT}`);

const tsOutput = buildTypeScript(source);
writeFileSync(TS_OUT, tsOutput);
console.log(`  TS  -> ${TS_OUT}`);

// Verify CSS has double-hyphen naming
if (!cssOutput.includes('--ppl-color-passion--primary')) {
  console.error('ERROR: CSS output missing --ppl-color-passion--primary');
  process.exit(1);
}

console.log('Build complete.');
console.log(`  Verified: --ppl-color-passion--primary present in CSS`);
